window.setLang = (lang) => {
  localStorage.setItem('preferred_lang', lang);
  
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  const langBtn = document.getElementById('langBtn');
  const langPopover = document.getElementById('langPopover');

  const btnSpan = langBtn ? langBtn.querySelector('span') : null;
  if (btnSpan && translations[lang]) {
    btnSpan.textContent = translations[lang].current_lang;
  }

  if (langPopover) {
    langPopover.querySelectorAll('.lang-item').forEach(item => {
      item.classList.remove('selected');
      const oc = item.getAttribute('onclick');
      if (oc && oc.includes(`setLang('${lang}')`)) {
        item.classList.add('selected');
      }
    });

    langPopover.classList.remove('show');
    if (langBtn) {
      langBtn.classList.remove('active');
      langBtn.setAttribute('aria-expanded', 'false');
    }
  }

  if (lang === 'ar') {
    document.body.dir = 'rtl';
    document.body.classList.add('rtl-mode');
    const navbar = document.getElementById('navbar');
    if(navbar) navbar.style.direction = 'rtl';
  } else {
    document.body.dir = 'ltr';
    document.body.classList.remove('rtl-mode');
    const navbar = document.getElementById('navbar');
    if(navbar) navbar.style.direction = 'ltr';
  }
};

document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('preferred_lang') || 'fr';
  if(window.setLang) window.setLang(savedLang);
});
